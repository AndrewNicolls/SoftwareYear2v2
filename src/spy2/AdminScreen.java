            String studentID = txfStuID.getText();
            String firstName = txfName.getText();
            String surname = txfSurname.getText();
            
            int homeNum = Integer.parseInt(txfhomeNo.getText()) ;
            int cellNum = Integer.parseInt(txfContact.getText());
            String email = txfEmail.getText();
            
            
            
