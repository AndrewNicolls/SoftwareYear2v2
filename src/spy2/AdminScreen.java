    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       
         String search = txfSearch.getText();
         int check =0;// to see if search matches
         

       for(int i = 0; i < AdminStudentTbl.getRowCount(); i++){//For each row
        for(int j = 0; j < AdminStudentTbl.getColumnCount(); j++){//For each column in that row
            if(AdminStudentTbl.getModel().getValueAt(i, j).equals(search)){//Search the model
               
                AdminStudentTbl.setRowSelectionInterval(i, i);//selects row from adminStudentTable that matches search
                check = 1;
            }
             
        }
   }   
       if(check!=1){
             JOptionPane.showMessageDialog(rootPane, "This record does not exist");
             
            }
            
          txfSearch.setText("");//reset search
         
        
    }//GEN-LAST:event_btnSearchActionPerformed

